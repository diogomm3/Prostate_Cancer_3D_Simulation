// Declare a Variable

// To declare a variable we first need to provide two things: the variable type and the variable name
// To do this we need the following statment: 'int name;'
// The int command representes an integer variable, the name sets the variable name

#include <iostream>

int main() {
    int year;
    year=2019;
    return 0;
    //int year = 2019;
}

// After we define a variable we assign to it a value, in this case the variable year is going to be assigned the value 2019
// The '=' symbol means assign, not equal as we should expect
// Both the previous commands can be combined into one simple statment 'int year = 2019;'